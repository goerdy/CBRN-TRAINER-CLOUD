<div class="container">
    <h2 style="text-align: center; margin-top: 0;">Anleitung zum CBRN-TRAINER</h2>

    <div class="instruction-section">
        <h2>Erste Schritte</h2>
        <p>
            Willkommen zur Anleitung des CBRN-TRAINERs. Hier erfahren Sie, wie Sie den Trainer für Ihre Ausbildung einsetzen können.
            Der CBRN-TRAINER besteht aus zwei Komponenten: der Trainer-Ansicht zur Steuerung der Messwerte und der Trainee-Ansicht zur Darstellung der Messgeräte.
        </p>

        <h3>Schnellstart</h3>
        <ol>
            <li>Öffnen Sie die <a href="c/index.php">Cloud-Version</a> des CBRN-TRAINERs</li>
            <li>Wählen Sie "Als Trainer starten", um eine neue Übungssession zu erstellen</li>
            <li>Teilen Sie die generierte Session-ID mit Ihren Teilnehmern</li>
            <li>Die Teilnehmer öffnen die Cloud-Version und geben die Session-ID ein</li>
            <li>Steuern Sie als Trainer die Messwerte, die Teilnehmer sehen diese auf ihren Geräten</li>
        </ol>
    </div>

    <div class="instruction-section">
        <h2>A-Einsatz (Radioaktivität)</h2>
        
        <h3>Trainer-Ansicht</h3>
        <p>
            In der A-Einsatz-Ansicht können Sie folgende Parameter einstellen:
        </p>
        <ul>
            <li><strong>Quellstärke:</strong> Bestimmt die Stärke der simulierten Strahlenquelle</li>
            <li><strong>Abstand:</strong> Simuliert den Abstand zur Strahlenquelle</li>
            <li><strong>Abschirmung:</strong> Simuliert verschiedene Abschirmmaßnahmen</li>
        </ul>

        <h3>Trainee-Ansicht</h3>
        <p>
            Die Trainees sehen folgende Messgeräte:
        </p>
        <ul>
            <li><strong>Dosisleistungsmessgerät:</strong> Zeigt die aktuelle Dosisleistung an</li>
            <li><strong>Dosiswarngerät:</strong> Warnt bei Überschreitung bestimmter Schwellenwerte</li>
            <li><strong>DL-Warner:</strong> Akustische und optische Warnung bei hoher Dosisleistung</li>
        </ul>
    </div>

    <div class="instruction-section">
        <h2>C-Einsatz (Chemische Stoffe)</h2>
        
        <h3>Trainer-Ansicht</h3>
        <p>
            In der C-Einsatz-Ansicht können Sie folgende Parameter einstellen:
        </p>
        <ul>
            <li><strong>CO:</strong> Kohlenmonoxid-Konzentration</li>
            <li><strong>CO2:</strong> Kohlendioxid-Konzentration</li>
            <li><strong>O2:</strong> Sauerstoff-Konzentration</li>
            <li><strong>CH4:</strong> Methan-Konzentration</li>
            <li><strong>H2S:</strong> Schwefelwasserstoff-Konzentration</li>
            <li><strong>NH3:</strong> Ammoniak-Konzentration</li>
        </ul>

        <h3>Trainee-Ansicht</h3>
        <p>
            Die Trainees sehen folgende Messgeräte:
        </p>
        <ul>
            <li><strong>CO-Warngerät:</strong> Zeigt die CO-Konzentration an</li>
            <li><strong>Multiwarngerät:</strong> Zeigt verschiedene Gaskonzentrationen an</li>
        </ul>

        <div class="note">
            <strong>Hinweis:</strong> Sie können vordefinierte Szenarien wie "Brand" oder "Gasleck" auswählen, um realistische Messwerte zu simulieren.
        </div>
    </div>

    <div class="instruction-section">
        <h2>Android-App</h2>
        
        <h3>Installation</h3>
        <p>
            Die Android-App bietet zusätzliche Funktionen für realistische Übungen im Gelände:
        </p>
        <ol>
            <li>Laden Sie die App herunter (siehe <a href="a/index.php">App-Seite</a>)</li>
            <li>Installieren Sie die APK-Datei auf Ihrem Android-Gerät</li>
            <li>Erteilen Sie die erforderlichen Berechtigungen (Bluetooth, Sensoren)</li>
        </ol>

        <h3>Bluetooth-Beacon für Dosisleistungsmessung</h3>
        <p>
            Mit Bluetooth-Beacons können Sie realistische Übungen zur Dosisleistungsmessung durchführen:
        </p>
        <ol>
            <li>Platzieren Sie einen Bluetooth-Beacon als "Strahlenquelle"</li>
            <li>Die App zeigt je nach Abstand zum Beacon unterschiedliche Dosisleistungen an</li>
            <li>Trainees können so lernen, wie man sich einer Strahlenquelle nähert und Messungen durchführt</li>
        </ol>

        <h3>Magnete für Kontaminationsnachweis</h3>
        <p>
            Mit Magneten können Sie Kontaminationen simulieren:
        </p>
        <ol>
            <li>Verstecken Sie Neodym-Magnete an Personen oder Gegenständen</li>
            <li>Die App erkennt diese Magnete als "Kontamination"</li>
            <li>Trainees können so den Umgang mit Kontaminationsmessgeräten üben</li>
        </ol>

        <div class="warning">
            <strong>Achtung:</strong> Achten Sie darauf, dass starke Magnete elektronische Geräte beschädigen können. Halten Sie sie von Kreditkarten, Herzschrittmachern und anderen empfindlichen Geräten fern.
        </div>
    </div>
</div> 