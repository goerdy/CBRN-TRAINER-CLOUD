    <h2 style="text-align: center; margin-top: 0;">Häufig gestellte Fragen (FAQ)</h2>

    <h2>Allgemeine Fragen</h2>
    
    <div class="faq-item">
        <div class="faq-question">Warum wurde der CBRN-TRAINER entwickelt?</div>
        <div class="faq-answer">
            Häufig läuft CBRN-Ausbildung so ab, dass der Ausbilder sagt: "Stell dir vor, dein Messgerät zeigt jetzt XY an" oder es kleben Postits mit Messwerten auf dem Boden. Messgeräte-Simulatoren der Hersteller sind zwar hervorragend, für die meisten Organisationen aber nicht finanzierbar. Aus diesem Grund habe ich dieses Projekt ins Leben gerufen - um eine kostenlose Alternative anzubieten, die die Ausbildung realistischer und effektiver gestaltet.
        </div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Was ist der CBRN-TRAINER?</div>
        <div class="faq-answer">
            Der CBRN-TRAINER ist eine Simulationsplattform für Messgeräte im Bereich der Gefahrenabwehr. Er besteht aus einer Trainer-Komponente, mit der Ausbilder Messwerte vorgeben können, und einer Trainee-Komponente, die diese Werte auf realitätsnah gestalteten Messgeräten anzeigt. So können realistische Übungsszenarien erstellt werden, ohne teure Messgeräte oder gefährliche Stoffe einsetzen zu müssen.
        </div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Ist der CBRN-TRAINER wirklich kostenlos?</div>
        <div class="faq-answer">
            Ja, der CBRN-TRAINER ist und bleibt ein vollständig kostenloses Angebot für Feuerwehren, Hilfsorganisationen, THW und andere Einsatzkräfte. Das Projekt wird als privates Bildungsprojekt ohne kommerzielle Interessen betrieben. Ich freue mich jedoch sehr über Klett-Patches eurer Organisationen als kleines Dankeschön. Auch Bilder, wie das Tool in der Praxis eingesetzt wird, würden mich sehr glücklich machen und motivieren, das Projekt weiterzuentwickeln.
        </div>
    </div>

    <h2>Cloud-Version</h2>
    
    <div class="faq-item">
        <div class="faq-question">Wie funktioniert die Cloud-Version?</div>
        <div class="faq-answer">
            Die Cloud-Version läuft direkt im Browser ohne Installation. Der Ausbilder (Trainer) erstellt eine Session und steuert die Messwerte für Gase oder Strahlung über ein Webinterface. Die Teilnehmer (Trainees) können mit der Session-ID auf die simulierten Messgeräte zugreifen und die Werte auf realitätsnah gestalteten Messgeräten ablesen. So können mehrere Teilnehmer gleichzeitig mit verschiedenen Geräten üben, während der Ausbilder die Werte zentral steuert.
        </div>
    </div>

    <h2>Android-App</h2>
    
    <div class="faq-item">
        <div class="faq-question">Wann wird die App im Google Play Store verfügbar sein?</div>
        <div class="faq-answer">
            Der Anmeldeprozess im Google Play Store ist noch nicht abgeschlossen. Wir arbeiten daran, die App so bald wie möglich offiziell verfügbar zu machen. Bis dahin können Sie sich als Beta-Tester anmelden.
        </div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Wie funktioniert die Dosisleistungsmessung mit Bluetooth Beacons?</div>
        <div class="faq-answer">
            Die App kann mit Bluetooth Beacons kommunizieren, die als "Strahlenquellen" platziert werden können. Je näher das Smartphone am Beacon ist, desto höher ist die angezeigte Dosisleistung. Dies ermöglicht realistische Übungen im Gelände. Eine genauere Beschreibung, welche Beacons gut funktionieren, wird noch ergänzt. Prinzipiell funktioniert jeder Beacon, der häufig seine ID aussendet. Dies ist sowohl als Selbstbau mit einem ESP32-Chip möglich als auch mit fertigen Beacons, die für etwa 10-15€ erhältlich sind.
        </div>
    </div>
    
    <div class="faq-item">
        <div class="faq-question">Welche Magnete werden für den Kontaminationsnachweis benötigt?</div>
        <div class="faq-answer">
            Für den Kontaminationsnachweis können grundsätzlich normale Magnete verwendet werden. Diese sind jedoch oft zu schwach, wenn sie unter der Kleidung oder z.B. im Schuh versteckt werden. Daher bietet es sich an, Neodym-Magnete zu nutzen, die man im 10er-Pack für wenige Euro bekommt. Die App erkennt diese Magnete mit dem Magnetometer des Smartphones als "Kontamination", was realistische Übungen zur Personendekontamination ermöglicht.
        </div>
    </div>

