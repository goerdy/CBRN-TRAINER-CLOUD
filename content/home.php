<h2>Über das Projekt</h2>
<p>
    Der CBRN-TRAINER ist ein kostenloses Bildungsprojekt zur Unterstützung der Ausbildung von Einsatzkräften im Umgang mit Messgeräten für chemische und radioaktive Gefahrstoffe.
    Das System besteht aus zwei Komponenten: einer Trainer-Ansicht, in der Ausbilder Messwerte für Gase oder Strahlung vorgeben können, und einer Trainee-Ansicht, 
    die diese Werte auf realitätsnah gestalteten Messgeräten darstellt. So können realistische Übungsszenarien erstellt werden, ohne teure Messgeräte oder gefährliche Stoffe einsetzen zu müssen.
</p>

