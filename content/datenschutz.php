<script>
function showLanguage(lang) {
    // Hide all content
    document.getElementById('deContent').style.display = 'none';
    document.getElementById('enContent').style.display = 'none';
    
    // Remove active class from all buttons
    document.getElementById('deBtn').classList.remove('active');
    document.getElementById('enBtn').classList.remove('active');
    
    // Show selected language content
    if (lang === 'de') {
        document.getElementById('deContent').style.display = 'block';
        document.getElementById('deBtn').classList.add('active');
    } else {
        document.getElementById('enContent').style.display = 'block';
        document.getElementById('enBtn').classList.add('active');
    }
}
</script>
